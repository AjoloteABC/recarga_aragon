class GlobalVariables {
  static String uid = '';
  static String name = '';
  static String email = '';
  static String cuenta = '';

  static void setUID(String value) {
    uid = value;
  }

  static String getUID() {
    return uid;
  }

  static void setName(String value) {
    name = value;
  }

  static String getName() {
    return name;
  }
  static void setEmail(String value) {
    email = value;
  }

  static String getEmail() {
    return email;
  }


  static void setCuenta(String value) {
    cuenta = value;
  }

  static String getCuenta() {
    return cuenta;
  }
}